package com.weatherforecast.domain.models;

public class Coord {
    private double lon;
    private double lat;

    public double getLon() {
        return lon;
    }

    public void setLon(long value) {
        this.lon = value;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(long value) {
        this.lat = value;
    }
}
